#include <stdio.h>
#include <stdlib.h>

void main(){

    int a = 6, b = 3;

    //Add
    printf("A soma de %d e %d eh: %d", a, b, a + b);//The add of %d and %d is

    //Sub
    printf("\nA subtracao de %d e %d eh: %d", a, b, a - b);//The sub of %d and %d is

    //Division
    printf("\nA divisao de %d e %d eh: %d", a, b, a / b);//The division of %d and %d is

    //Multiplication
    printf("\nA multiplicacao de %d e %d eh: %d", a, b, a * b);//The multiplication of %d and %d is

    //Rest of Division
    printf("\nO resto da divisao entre %d e %d eh: %d", a, b, a % b);//The rest of division of %d and %d is

    //Absolute value
    printf("\nO valor absoluto de -3 eh: %d", abs(-3));//The absolute value of -3 is

}
